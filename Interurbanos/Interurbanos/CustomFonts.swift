import UIKit

extension UIFont
{
    static var titleFont: UIFont {
        return UIFont.boldSystemFont(ofSize: 18.0)
    }
    
    static var defaultFont: UIFont {
        return UIFont.systemFont(ofSize: 16.0)
    }
}
